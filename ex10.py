# (Calculadora de Área de Retângulo) Receba a base e a altura de um retângulo e exiba a área dele usando a fórmula:
print(f"***CALCULADORA DE ÁREA DE RETANGULO!***")
base = float(input("Digite a base de um retângulo:"))
altura = float(input("Digite a altura de um retângulo:"))
area = base * altura 
print(f" A área do retangulo é {area}")